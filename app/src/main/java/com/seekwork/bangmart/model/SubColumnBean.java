package com.seekwork.bangmart.model;

import java.io.Serializable;

public class SubColumnBean implements Serializable {
    private String id;
    private String name;
    private int resId;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getResId() {
        return resId;
    }

    public void setResId(int resId) {
        this.resId = resId;
    }
}
